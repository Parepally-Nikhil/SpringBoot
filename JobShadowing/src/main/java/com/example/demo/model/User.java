package com.example.demo.model;

public class User {
	
	private String uid;
	private String uname;
	private String utechnology;
	private int uyoe;
	private long umobile;
	private String uemailId;
	private String upassword;
	private String uaddress;
	
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public String getUtechnologies() {
		return utechnology;
	}
	public void setUtechnology(String utechnology) {
		this.utechnology = utechnology;
	}
	public int getUyoe() {
		return uyoe;
	}
	public void setUyoe(int uyoe) {
		this.uyoe = uyoe;
	}
	public long getUmobile() {
		return umobile;
	}
	public void setUmobile(long umobile) {
		this.umobile = umobile;
	}
	public String getUemailId() {
		return uemailId;
	}
	public void setUemailId(String uemailId) {
		this.uemailId = uemailId;
	}
	public String getUpassword() {
		return upassword;
	}
	public void setUpassword(String upassword) {
		this.upassword = upassword;
	}
	public String getUaddress() {
		return uaddress;
	}
	public void setUaddress(String uaddress) {
		this.uaddress = uaddress;
	}

}
