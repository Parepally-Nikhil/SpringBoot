package com.example.demo.model;

public class ProfessionalUser {
	
	private String pid;
	private String pname;
	private String ptechnology;
	private int pyoe;
	private long pmobile;
	private String pemailId;
	private String ppassword;
	private String paddress;
	
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPtechnology() {
		return ptechnology;
	}
	public void setPtechnology(String ptechnology) {
		this.ptechnology = ptechnology;
	}
	public int getPyoe() {
		return pyoe;
	}
	public void setPyoe(int pyoe) {
		this.pyoe = pyoe;
	}
	public long getPmobile() {
		return pmobile;
	}
	public void setPmobile(long pmobile) {
		this.pmobile = pmobile;
	}
	public String getPemailId() {
		return pemailId;
	}
	public void setPemailId(String pemailId) {
		this.pemailId = pemailId;
	}
	public String getPpassword() {
		return ppassword;
	}
	public void setPpassword(String ppassword) {
		this.ppassword = ppassword;
	}
	public String getPaddress() {
		return paddress;
	}
	public void setPaddress(String paddress) {
		this.paddress = paddress;
	}
	
}
